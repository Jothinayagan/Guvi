import java.io.*;
import java.util.*;
class StarPattern1 
{
	public static void main (String[] args) 
	{
	    for(int i=0;i<5;i++)
	    {   
	        int j=0;
	        while(j<=4)
	        {
	            System.out.print("*");
	            j++;
	        }
	        System.out.println();
	    } 
	}
}
